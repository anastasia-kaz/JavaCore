    public String checkString(String string1, String string2) {
        String resultString;
        if (string1.endsWith(string2)) {
            resultString = string1.substring(0, string1.length() - string2.length());
        } else {
            resultString = string1.toUpperCase();
        }
        return resultString;
    }


